<?php


if ( ! class_exists( 'Timber' ) ) {
	add_action( 'admin_notices', function() {
		echo '<div class="error"><p>Timber not activated. Make sure you activate the plugin in <a href="' . esc_url( admin_url( 'plugins.php#timber' ) ) . '">' . esc_url( admin_url( 'plugins.php') ) . '</a></p></div>';
	});

	add_filter('template_include', function($template) {
		return get_stylesheet_directory() . '/static/no-timber.html';
	});

	return;
}

Timber::$dirname = array('templates', 'views');

class StarterSite extends TimberSite {

	function __construct() {
		add_theme_support( 'post-formats' );
		add_theme_support( 'post-thumbnails' );
		add_theme_support( 'menus' );
		add_action('after_setup_theme', array($this, 'register_menus'));
		add_theme_support( 'html5', array( 'comment-list', 'comment-form', 'search-form', 'gallery', 'caption' ) );
		add_filter( 'timber_context', array( $this, 'add_to_context' ) );
		add_filter( 'get_twig', array( $this, 'add_to_twig' ) );
		add_filter('style_loader_src', array($this, 'remove_wp_ver_css_js'), 9999 );
		add_filter('script_loader_src', array($this, 'remove_wp_ver_css_js'), 9999 );
		add_action( 'init', array( $this, 'register_post_types' ) );
		add_action( 'init', array( $this, 'register_taxonomies' ) );
		add_action('wp_enqueue_scripts', array($this, 'wp_enq_style_scripts'));
		parent::__construct();
	}

	// Enqueue custom styles and scripts in one place
	function wp_enq_style_scripts() {
		wp_deregister_script('jquery');
		wp_enqueue_style('css', get_template_directory_uri().'/assets/css/app.css');
		wp_enqueue_script('js', get_template_directory_uri().'/assets/js/app.js', array());
	}

	function register_menus(){
		register_nav_menus( array(
			'main' => 'Main Menu',
			'main_mobile' => 'Main Menu Mobile',
		));
	}

	function register_post_types() {
		//this is where you can register custom post types
	}

	function register_taxonomies() {
		//this is where you can register custom taxonomies
	}

	function remove_wp_ver_css_js( $src ) {
    if ( strpos( $src, 'ver=' ) ) {
      $src = remove_query_arg( 'ver', $src );
		}
    return $src;
	}

	function add_to_context( $context ) {
		$context['foo'] = 'bar';
		$context['stuff'] = 'I am a value set in your functions.php file';
		$context['notes'] = 'These values are available everytime you call Timber::get_context();';
		$context['main'] = new TimberMenu('main');
		$context['main_mobile'] = new TimberMenu('main_mobile');
		$context['site'] = $this;
		if ( is_front_page() ) {
			$context['front_page_title'] = get_field('title');
		}
		return $context;
	}

	function myfoo( $text ) {
		$text .= ' bar!';
		return $text;
	}

	function add_to_twig( $twig ) {
		/* this is where you can add your own functions to twig */
		$twig->addExtension( new Twig_Extension_StringLoader() );
		$twig->addFilter('myfoo', new Twig_SimpleFilter('myfoo', array($this, 'myfoo')));
		return $twig;
	}

}

new StarterSite();
